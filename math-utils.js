export function addition(x,y) {
    const sum = (x + y);
    return sum;
}

export function subtraction(x,y) {
    const difference = (x - y);
    return difference;
}

export function multiplication(x,y) {
    const product = (x * y);
    return product;
}

export function division(x,y) {
    const quotient = (x / y);
    return quotient;
}